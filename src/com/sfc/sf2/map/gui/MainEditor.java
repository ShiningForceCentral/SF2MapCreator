        BlockSlotPanel rightSlotBlockPanel = new BlockSlotPanel();
        baseBlocksLayout.setLeftSlotBlockPanel(leftSlotBlockPanel);
        baseBlocksLayout.setRightSlotBlockPanel(rightSlotBlockPanel);
